class Formatting():
    # Critical Logging Messages Colors:
    ERROR_BOLD = '\033[31;1m'
    ERROR_BOLD_UNDERLINE = '\033[31;1;4m'

    # Error Logging Messages Colors:
    ERROR_THIN = '\033[31m'
    ERROR_THIN_UNDERLINE = '\033[31;4m'

    # Warning Logging Messages Colors:
    WARNING_THIN = '\033[33m'
    WARNING_BOLD = '\033[33;1m'
    WARNING_THIN_UNDERLINE = '\033[33;4m'
    WARNING_BOLD_UNDERLINE = '\033[33;1;4m'

    # Info Logging Messages Colors:
    INFO_THIN = '\033[34m'
    INFO_BOLD = '\033[34;1m'
    INFO_THIN_UNDERLINE = '\033[34;4m'
    INFO_BOLD_UNDERLINE = '\033[34;1;4m'

    # Debug Logging Messages Colors:
    DEBUG_THIN = '\033[36m'
    DEBUG_BOLD = '\033[36;1m'
    DEBUG_THIN_UNDERLINE = '\033[36;4m'
    DEBUG_BOLD_UNDERLINE = '\033[36;1;4m'

    # Success Logging Messages Colors:
    SUCCESS_THIN = '\033[32m'
    SUCCESS_BOLD = '\033[32;1m'
    SUCCESS_THIN_UNDERLINE = '\033[32;4m'
    SUCCESS_BOLD_UNDERLINE = '\033[32;1;4m'

    # Add-Ons:
    STOP = '\033[0m'
    UNDERLINE = '\033[4m'
    BOLD = '\033[1m'
